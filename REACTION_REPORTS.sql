CREATE TABLE USER_TB (
    USER_ID       NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_USER_TB PRIMARY KEY,
    EMAIL         VARCHAR2(100) NOT NULL,
    PASSWORD      VARCHAR2(200) NOT NULL,
    NICKNAME      VARCHAR2(50) NOT NULL,
    PROFILE_IMAGE VARCHAR2(300),
    PHONE         VARCHAR2(20),
    BIRTH_YEAR    NUMBER(4),
    GENDER        VARCHAR2(10),
    CREATED_AT    DATE DEFAULT SYSDATE,
    STATUS        VARCHAR2(20) DEFAULT 'ACTIVE',

    CONSTRAINT UQ_USER_EMAIL UNIQUE (EMAIL),
    CONSTRAINT UQ_USER_NICKNAME UNIQUE (NICKNAME),
    CONSTRAINT UQ_USER_PHONE UNIQUE (PHONE)
);

CREATE TABLE ADMINS (
    ADMIN_ID   NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_ADMINS PRIMARY KEY,
    USER_ID    NUMBER UNIQUE NOT NULL,
    ROLE       VARCHAR2(30) DEFAULT 'manager',
    CREATED_AT DATE DEFAULT SYSDATE,

    CONSTRAINT FK_ADMINS_USER FOREIGN KEY (USER_ID)
        REFERENCES USER_TB (USER_ID)
);

CREATE TABLE CATEGORY (
    CATEGORY_ID  NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_CATEGORY PRIMARY KEY,
    NAME         VARCHAR2(100) NOT NULL UNIQUE,
    DESCRIPTION  VARCHAR2(300),
    CREATED_AT   DATE DEFAULT SYSDATE,
    UPDATED_AT   DATE
);


CREATE TABLE POSTS (
    POST_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_POSTS PRIMARY KEY,
    USER_ID        NUMBER NOT NULL,
    CATEGORY_ID    NUMBER,
    TITLE          VARCHAR2(200) NOT NULL,
    CONTENT        CLOB NOT NULL,
    VIEWS          NUMBER DEFAULT 0,
    IMAGE_URL      CLOB,
    VIDEO_URL      VARCHAR2(300),
    YOUTUBE_URL    VARCHAR2(300),
    MAP_LOCATION   VARCHAR2(300),
    TAGS           VARCHAR2(500),
    CREATED_AT     DATE DEFAULT SYSDATE,
    UPDATED_AT     DATE,

    CONSTRAINT FK_POSTS_USER FOREIGN KEY (USER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_POSTS_CATEGORY FOREIGN KEY (CATEGORY_ID)
        REFERENCES CATEGORY (CATEGORY_ID)
);


CREATE TABLE COMMENTS (
    COMMENT_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_COMMENTS PRIMARY KEY,
    USER_ID            NUMBER NOT NULL,
    POST_ID            NUMBER NOT NULL,
    PARENT_COMMENT_ID  NUMBER,
    CONTENT            CLOB NOT NULL,
    IMAGE_URL          VARCHAR2(300),
    CREATED_AT         DATE DEFAULT SYSDATE,
    UPDATED_AT         DATE,

    CONSTRAINT FK_COMMENTS_USER FOREIGN KEY (USER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_COMMENTS_POST FOREIGN KEY (POST_ID)
        REFERENCES POSTS (POST_ID),

    CONSTRAINT FK_COMMENTS_PARENT FOREIGN KEY (PARENT_COMMENT_ID)
        REFERENCES COMMENTS (COMMENT_ID)
);

CREATE TABLE POST_REACTIONS (
    REACTION_ID    NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_POST_REACTIONS PRIMARY KEY,
    USER_ID        NUMBER NOT NULL,
    POST_ID        NUMBER NOT NULL,
    REACTION_TYPE  VARCHAR2(20) NOT NULL,
    CREATED_AT     DATE DEFAULT SYSDATE,

    CONSTRAINT FK_POST_REACTIONS_USER FOREIGN KEY (USER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_POST_REACTIONS_POST FOREIGN KEY (POST_ID)
        REFERENCES POSTS (POST_ID),

    CONSTRAINT UQ_POST_REACTIONS UNIQUE (USER_ID, POST_ID, REACTION_TYPE)
);

CREATE TABLE COMMENT_REACTIONS (
    REACTION_ID    NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_COMMENT_REACTIONS PRIMARY KEY,
    USER_ID        NUMBER NOT NULL,
    COMMENT_ID     NUMBER NOT NULL,
    REACTION_TYPE  VARCHAR2(20) NOT NULL,
    CREATED_AT     DATE DEFAULT SYSDATE,

    CONSTRAINT FK_COMMENT_REACTIONS_USER FOREIGN KEY (USER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_COMMENT_REACTIONS_COMMENT FOREIGN KEY (COMMENT_ID)
        REFERENCES COMMENTS (COMMENT_ID),

    CONSTRAINT UQ_COMMENT_REACTIONS UNIQUE (USER_ID, COMMENT_ID, REACTION_TYPE)
);

CREATE TABLE POST_REPORTS (
    REPORT_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_POST_REPORTS PRIMARY KEY,
    REPORTER_ID      NUMBER NOT NULL,
    TARGET_USER_ID   NUMBER NOT NULL,
    POST_ID          NUMBER NOT NULL,
    REPORT_CATEGORY  VARCHAR2(50) NOT NULL,
    REASON           CLOB,
    EVIDENCE_URL     VARCHAR2(500),
    STATUS           VARCHAR2(20) DEFAULT 'pending',
    ADMIN_ID         NUMBER,
    CREATED_AT       DATE DEFAULT SYSDATE,
    PROCESSED_AT     DATE,

    CONSTRAINT FK_POST_REPORTS_REPORTER FOREIGN KEY (REPORTER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_POST_REPORTS_TARGET_USER FOREIGN KEY (TARGET_USER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_POST_REPORTS_POST FOREIGN KEY (POST_ID)
        REFERENCES POSTS (POST_ID),

    CONSTRAINT FK_POST_REPORTS_ADMIN FOREIGN KEY (ADMIN_ID)
        REFERENCES ADMINS (ADMIN_ID),

    CONSTRAINT UQ_POST_REPORTS UNIQUE (REPORTER_ID, POST_ID)
);

CREATE TABLE COMMENT_REPORTS (
    REPORT_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_COMMENT_REPORTS PRIMARY KEY,
    REPORTER_ID      NUMBER NOT NULL,
    TARGET_USER_ID   NUMBER NOT NULL,
    COMMENT_ID       NUMBER NOT NULL,
    REPORT_CATEGORY  VARCHAR2(50) NOT NULL,
    REASON           CLOB,
    EVIDENCE_URL     VARCHAR2(500),
    STATUS           VARCHAR2(20) DEFAULT 'pending',
    ADMIN_ID         NUMBER,
    CREATED_AT       DATE DEFAULT SYSDATE,
    PROCESSED_AT     DATE,

    CONSTRAINT FK_COMMENT_REPORTS_REPORTER FOREIGN KEY (REPORTER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_COMMENT_REPORTS_TARGET_USER FOREIGN KEY (TARGET_USER_ID)
        REFERENCES USER_TB (USER_ID),

    CONSTRAINT FK_COMMENT_REPORTS_COMMENT FOREIGN KEY (COMMENT_ID)
        REFERENCES COMMENTS (COMMENT_ID),

    CONSTRAINT FK_COMMENT_REPORTS_ADMIN FOREIGN KEY (ADMIN_ID)
        REFERENCES ADMINS (ADMIN_ID),

    CONSTRAINT UQ_COMMENT_REPORTS UNIQUE (REPORTER_ID, COMMENT_ID)
);
